#include "libvec.h"

t_vec3	v3_cross(t_vec3 v1, t_vec3 v2)
{
	t_vec3	new_v;

	new_v.x = v1.y * v2.z - v1.z * v2.y;
	new_v.y = v1.z * v2.x - v1.x * v2.z;
	new_v.z = v1.x * v2.y - v1.y * v2.x;
	return (new_v);
}
